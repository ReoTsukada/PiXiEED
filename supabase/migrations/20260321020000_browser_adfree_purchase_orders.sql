create table if not exists public.browser_adfree_purchase_orders (
  id bigint generated by default as identity primary key,
  provider text not null default 'stores',
  provider_order_id text not null,
  product_key text not null default 'browser_ad_free',
  buyer_email text not null,
  payment_status text not null default 'pending',
  code text references public.user_entitlement_codes(code) on delete set null,
  issued_at timestamptz,
  claimed_at timestamptz,
  claimed_by uuid references auth.users(id) on delete set null,
  raw_payload jsonb not null default '{}'::jsonb,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (provider, provider_order_id, product_key)
);

