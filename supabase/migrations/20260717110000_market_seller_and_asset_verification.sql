-- Harden the inheritance market before its first remote deployment.
-- Authentication, seller identity, listing review, and file provenance are
-- deliberately separate signals. None of them alone guarantees copyright.

create table if not exists public.market_asset_formats (
  id text primary key,
  label text not null,
  media_kind text not null check (media_kind in ('project', 'image', 'animation')),
  allows_pixieed_native boolean not null default false,
  allows_external_upload boolean not null default false,
  active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now())
);

insert into public.market_asset_formats (
  id, label, media_kind, allows_pixieed_native, allows_external_upload
) values
  ('pixiedraw-project', 'PiXiEEDrawプロジェクト', 'project', true, false),
  ('png', 'PNG画像', 'image', true, true),
  ('webp', 'WebP画像', 'image', true, true),
  ('gif', 'GIFアニメーション', 'animation', true, true),
  ('apng', 'APNGアニメーション', 'animation', true, true),
  ('sprite-sheet-png', 'PNGスプライトシート', 'animation', true, true)
on conflict (id) do update set
  label = excluded.label,
  media_kind = excluded.media_kind,
  allows_pixieed_native = excluded.allows_pixieed_native,
  allows_external_upload = excluded.allows_external_upload;

alter table public.market_asset_formats enable row level security;
create policy market_asset_formats_read_active
on public.market_asset_formats for select
to anon, authenticated
using (active);

create table if not exists public.market_seller_profiles (
  user_id uuid primary key references auth.users(id) on delete restrict,
  seller_status text not null default 'pending'
    check (seller_status in ('pending', 'verified', 'restricted', 'disabled')),
  identity_status text not null default 'unverified'
    check (identity_status in ('unverified', 'pending', 'verified', 'failed', 'expired')),
  terms_version text,
  terms_accepted_at timestamptz,
  verified_at timestamptz,
  restricted_reason text not null default '',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  check (
    seller_status <> 'verified'
    or (identity_status = 'verified' and terms_accepted_at is not null)
  )
);

alter table public.market_seller_profiles enable row level security;
create policy market_seller_profiles_read_own
on public.market_seller_profiles for select
to authenticated
using (user_id = auth.uid());

create table if not exists public.market_staff_roles (
  user_id uuid not null references auth.users(id) on delete cascade,
  role text not null check (role in ('reviewer', 'admin')),
  active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  primary key (user_id, role)
);

alter table public.market_staff_roles enable row level security;

create table if not exists public.market_audit_log (
  id bigint generated by default as identity primary key,
  actor_user_id uuid references auth.users(id) on delete set null,
  action text not null,
  target_type text not null,
  target_id text not null,
  details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  check (jsonb_typeof(details) = 'object')
);

alter table public.market_audit_log enable row level security;
create index if not exists market_audit_log_target_idx
  on public.market_audit_log(target_type, target_id, created_at desc);

alter table public.market_assets
  add column if not exists source_kind text not null default 'external',
  add column if not exists source_sha256 text,
  add column if not exists provenance_manifest jsonb not null default '{}'::jsonb,
  add column if not exists verification_status text not null default 'pending',
  add column if not exists verification_level text not null default 'unverified',
  add column if not exists file_scan_status text not null default 'pending',
  add column if not exists seller_identity_verified boolean not null default false,
  add column if not exists verified_at timestamptz,
  add column if not exists verified_by uuid references auth.users(id) on delete set null;

alter table public.market_assets
  add constraint market_assets_source_kind_check
    check (source_kind in ('pixieed-native', 'external')),
  add constraint market_assets_source_sha256_check
    check (source_sha256 is null or source_sha256 ~ '^[0-9a-f]{64}$'),
  add constraint market_assets_provenance_manifest_check
    check (jsonb_typeof(provenance_manifest) = 'object'),
  add constraint market_assets_verification_status_check
    check (verification_status in ('pending', 'verified', 'rejected', 'revoked')),
  add constraint market_assets_verification_level_check
    check (verification_level in ('unverified', 'pixieed-native', 'external-reviewed')),
  add constraint market_assets_file_scan_status_check
    check (file_scan_status in ('pending', 'clean', 'rejected', 'error')),
  add constraint market_assets_format_fk
    foreign key (asset_format) references public.market_asset_formats(id) on delete restrict;

create or replace function public.market_current_user_has_confirmed_identity()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    auth.uid() is not null
    and lower(coalesce(auth.jwt() ->> 'is_anonymous', 'false')) not in ('true', 't', '1')
    and coalesce(auth.jwt() -> 'app_metadata' ->> 'provider', '') <> 'anonymous'
    and exists (
      select 1
      from auth.users as users
      where users.id = auth.uid()
        and users.email_confirmed_at is not null
    );
$$;

create or replace function public.market_current_user_can_sell()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.market_current_user_has_confirmed_identity()
    and exists (
      select 1
      from public.market_seller_profiles as seller
      where seller.user_id = auth.uid()
        and seller.seller_status = 'verified'
        and seller.identity_status = 'verified'
        and seller.terms_accepted_at is not null
    );
$$;

create or replace function public.market_current_user_is_reviewer()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.market_staff_roles as staff
    where staff.user_id = auth.uid()
      and staff.active
      and staff.role in ('reviewer', 'admin')
  );
$$;

revoke all on function public.market_current_user_has_confirmed_identity() from public, anon, authenticated;
revoke all on function public.market_current_user_can_sell() from public, anon, authenticated;
revoke all on function public.market_current_user_is_reviewer() from public, anon, authenticated;
grant execute on function public.market_current_user_has_confirmed_identity() to authenticated;
grant execute on function public.market_current_user_can_sell() to authenticated;
grant execute on function public.market_current_user_is_reviewer() to authenticated;

