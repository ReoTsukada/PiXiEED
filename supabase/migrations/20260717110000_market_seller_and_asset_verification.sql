create or replace function public.market_create_root_asset_v2(
  input_title text,
  input_description text,
  input_sale_price_yen integer,
  input_base_use_price_yen integer,
  input_required_option_price_yen integer,
  input_derivative_license_price_yen integer,
  input_derivative_sales_allowed boolean,
  input_source_kind text,
  input_source_sha256 text,
  input_asset_format text,
  input_provenance_manifest jsonb default '{}'::jsonb,
  input_inherited_terms jsonb default '{}'::jsonb,
  input_prohibited_uses jsonb default '[]'::jsonb,
  input_allowed_media jsonb default '[]'::jsonb,
  input_change_summary jsonb default '[]'::jsonb
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_asset_id uuid;
  v_format public.market_asset_formats%rowtype;
begin
  if not public.market_current_user_can_sell() then
    raise exception 'verified seller account required';
  end if;
  if input_source_kind not in ('pixieed-native', 'external') then
    raise exception 'unsupported source kind';
  end if;
  if coalesce(lower(input_source_sha256), '') !~ '^[0-9a-f]{64}$' then
    raise exception 'valid source sha256 required';
  end if;
  if jsonb_typeof(coalesce(input_provenance_manifest, '{}'::jsonb)) <> 'object' then
    raise exception 'invalid provenance manifest';
  end if;

  select * into v_format
  from public.market_asset_formats
  where id = input_asset_format and active;
  if not found
     or (input_source_kind = 'pixieed-native' and not v_format.allows_pixieed_native)
     or (input_source_kind = 'external' and not v_format.allows_external_upload) then
    raise exception 'asset format is not allowed for this source';
  end if;
  if input_source_kind = 'pixieed-native'
     and coalesce(input_provenance_manifest ->> 'schema', '') <> 'pixieed-market-manifest/v1' then
    raise exception 'PiXiEED native source requires a v1 provenance manifest';
  end if;

  v_asset_id := public.market_create_root_asset(
    input_title, input_description, input_sale_price_yen,
    input_base_use_price_yen, input_required_option_price_yen,
    input_derivative_license_price_yen, input_derivative_sales_allowed,
    input_inherited_terms, input_prohibited_uses, input_allowed_media,
    input_asset_format, input_change_summary
  );

  update public.market_assets
  set source_kind = input_source_kind,
      source_sha256 = lower(input_source_sha256),
      provenance_manifest = coalesce(input_provenance_manifest, '{}'::jsonb),
      verification_status = 'pending',
      verification_level = 'unverified',
      file_scan_status = 'pending'
  where id = v_asset_id;

  insert into public.market_audit_log(actor_user_id, action, target_type, target_id)
  values (auth.uid(), 'root_draft_created', 'market_asset', v_asset_id::text);
  return v_asset_id;
end;
$$;

